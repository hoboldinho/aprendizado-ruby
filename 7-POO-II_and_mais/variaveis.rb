# escopo de variável: define onde a variável estará disponível no programa

# - variável local
# declarada: primeira letra é minúcula ou sublinhado '_'
# pode ser acessada somente onde foi criada

# variável global
# declarada: por '#'
# fortemente desncorajada, já que pode sofrer alterações de tipo em vários lugares

# variável de classe
# declarada: por '@@' dentro de uma classe
# é compartilhada entre todas as instâncias

# variável de instancia
# declarada: por '@' dentro de uma instancia
# seu valor não é compartilhado entre as instâncias